export function getFishById(id: string): Fish | undefined {
  return FISH_DATABASE.find((f) => f.id === id);
}

export function checkCompatibility(
  fishIds: string[]
): { compatible: boolean; issues: string[] } {
  const issues: string[] = [];
  const fish = fishIds.map((id) => FISH_DATABASE.find((f) => f.id === id)).filter(Boolean) as Fish[];

  for (let i = 0; i < fish.length; i++) {
    for (let j = i + 1; j < fish.length; j++) {
      const a = fish[i];
      const b = fish[j];
      if (a.incompatibleWith.includes(b.id) || b.incompatibleWith.includes(a.id)) {
        issues.push(`${a.name} and ${b.name} are not compatible`);
      }
    }
  }

  return { compatible: issues.length === 0, issues };
}
